bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case LAUNCHY:
        if (record->event.pressed) {
            SEND_STRING(SS_LALT(" "));
        }
        break;
    case CMDER:
        if (record->event.pressed) {
            SEND_STRING(SS_LGUI(SS_LALT(" ")));
        }
        break;

    }
    return true;
}

